    private void funcionComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_funcionComboBoxActionPerformed
         String selectedFunction = (String) funcionComboBox.getSelectedItem();
        if ("Dibujar".equals(selectedFunction)) {
            ((MainPanel) jPanel1).setEditingMode(false);
            ((MainPanel) jPanel1).setMovingMode(false);
            ((MainPanel) jPanel1).setDeletingMode(false);
        } else if ("Redimensionar Figura".equals(selectedFunction)) {
            ((MainPanel) jPanel1).setEditingMode(true);
            ((MainPanel) jPanel1).setMovingMode(false);
            ((MainPanel) jPanel1).setDeletingMode(false);
        } else if ("Mover Figura".equals(selectedFunction)) {
            ((MainPanel) jPanel1).setEditingMode(false);
            ((MainPanel) jPanel1).setDeletingMode(false);
            ((MainPanel) jPanel1).setMovingMode(true);
        } else if ("Borrar Figura".equals(selectedFunction)) {
            ((MainPanel) jPanel1).setEditingMode(false);
            ((MainPanel) jPanel1).setMovingMode(false);
            ((MainPanel) jPanel1).setDeletingMode(true);
        }
    }//GEN-LAST:event_funcionComboBoxActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        double result = ((MainPanel) jPanel1).calculateRedAreaPercentage();
        int panelWidth = jPanel1.getWidth();
        int panelHeight = jPanel1.getHeight();
        List<Drawable> drawables = ((MainPanel) jPanel1).getShapes();
        List<Shape> shapes = new ArrayList<>();
        for (Drawable drawable : drawables) {
            if (drawable instanceof Shape) {
                shapes.add((Shape) drawable);
            }
        }
        ResultWindow resultWindow = new ResultWindow(image, shapes, result, panelWidth, panelHeight);
        resultWindow.setVisible(true);
    }//GEN-LAST:event_jButton5ActionPerformed
    
      
    
